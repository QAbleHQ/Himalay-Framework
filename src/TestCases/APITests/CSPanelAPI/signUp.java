    @Test(dataProvider = "login_email_password_Admin")
    public void signUp_for_Admin_User(String email, String password, String organizationId, String organizationName,String organization_provider_ids, String course_id) {


        RequestBuilder builder = new RequestBuilder();
        GetTokenUtility getToken = new GetTokenUtility();

        JSONObject header = new JSONObject();
        header.put("content-type", "application/json");
        header.put("x-authorization",getToken.getAccessToken(email, password));

        JSONObject body = new JSONObject();
        JSONObject pathParameter = new JSONObject();
        JSONObject queryParameters = new JSONObject();
        queryParameters.put("organization_id",organizationId.substring(0,3));
        JSONObject schema = new JSONObject();

        String update_file = builder.updateRequestObject("CSPanelAPI/signUp", pathParameter, queryParameters, header, body, schema);
        JSONObject response = builder.performRequest(update_file);
        System.out.println(response);

        ResponseValidator validator = new ResponseValidator(response);
        validator.statusCodeShouldBe(200);

        String total = validator.getDataFromBody("$.total");
        assertThat(total).isNotBlank();

        String active = validator.getDataFromBody("$.active");
        assertThat(active).isNotBlank();

        String not_active = validator.getDataFromBody("$.not_active");
        assertThat(not_active).isNotBlank();

        String percent_active = validator.getDataFromBody("$.percent_active");
        assertThat(percent_active).isNotBlank();

        String percent_not_active = validator.getDataFromBody("$.percent_not_active");
        assertThat(percent_not_active).isNotBlank();

    }

